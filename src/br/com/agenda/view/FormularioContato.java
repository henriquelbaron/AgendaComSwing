    public void adicionarNumeroTelefonesTabela(List<Telefone> telefones) {
        String[] colunas = {"DDD", "Numero", "Tipo"};
        String[][] dados = new String[telefones.size()][colunas.length];
        for (int i = 0; i < telefones.size(); i++) {
            Telefone t = telefones.get(i);
            dados[i][0] = t.getDDD();
            dados[i][1] = t.getNumero();
            dados[i][2] = t.getTipo();
        }
        DefaultTableModel modelo = new DefaultTableModel(dados, colunas);
        tableTelefones.setModel(modelo);
    }

    public void limparCampos() {
        tfNome.setText(null);
        tfEmail.setText(null);
        formatNascimento.setText(null);
        tfDdd.setText(null);
        tfNumeroTelefone.setText(null);
        telefones = new ArrayList<>();
        adicionarNumeroTelefonesTabela(telefones);
    }

    private boolean isNumber(String str) {
        Pattern p = Pattern.compile("[0-9]+");
        Matcher m = p.matcher(str);
        return m.find();
    }
    

