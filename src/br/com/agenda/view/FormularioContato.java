    private void bGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGravarActionPerformed
        if (tfNome.getText().isEmpty() || formatNascimento.getText().isEmpty()
                || tfEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha os campos obrigatorios!");

        } else {
            contato = new Contato();
            try {
                contato.setId(WIDTH);
                contato.setNome(tfNome.getText());
                contato.setEmail(tfEmail.getText());
                contato.setDataNascimento(df.parse(formatNascimento.getText()));
                tipoContato = (TipoContato) cbTipo.getSelectedItem();
                contato.setTipoContato(tipoContato);
                if (telefones != null) {
                    contato.setTelefones(telefones);
                }
                contatoDao = new ContatoDaoImpl();
                if (contatoAlterar == null) {
                    if (contatoDao.inserir(contato)) {
                        Integer escolha = JOptionPane.showConfirmDialog(null, "Contato Adicionado com Sucesso\n"
                                + "Deseja Adicionar outro Contato?", " ", JOptionPane.YES_NO_OPTION);
                        contato = null;
                        if (escolha.equals(0)) {
                            limparCampos();
                        } else {
                            Principal.listarContatos();
                        }
                    }
                } else {
                    contato.setId(contatoAlterar);
                    if (contatoDao.update(contato)) {
                        JOptionPane.showMessageDialog(null, "Contato Alterado com Sucesso");
                        Principal.listarContatos();
                        contato = null;
                    }
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_bGravarActionPerformed

