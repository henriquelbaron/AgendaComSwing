    private void bMaisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bMaisActionPerformed
        if (telefones == null) {
            telefones = new ArrayList<>();
        }
        if (tfDdd.getText().length() > 2 || tfDdd.getText().isEmpty()
                || tfNumeroTelefone.getText().length() > 10 || tfNumeroTelefone.getText().isEmpty()
                || !isNumber(tfNumeroTelefone.getText().toString())) {
            JOptionPane.showMessageDialog(null, "Campos Vazios não Sao aceitos\nDDD Com 2 Numeros\nTelefone com até 10 numeros");
        } else {
            for (Telefone telefone1 : telefones) {
                if (telefone1.getNumero().equalsIgnoreCase(tfNumeroTelefone.getText())) {
                    JOptionPane.showMessageDialog(null, "Esse Numero ja existe");
                    return;
                }
            }
            telefone = new Telefone();
            telefone.setDDD(tfDdd.getText());
            telefone.setNumero(tfNumeroTelefone.getText());
            telefone.setTipo(comboBoxTipoCelular.getItemAt(comboBoxTipoCelular.getSelectedIndex()));
            telefones.add(telefone);
            adicionarNumeroTelefonesTabela(telefones);

        }
    }//GEN-LAST:event_bMaisActionPerformed

